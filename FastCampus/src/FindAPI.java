public class FindAPI {
    public static void main(String[] args) {
        int a;
       //  AAA b;
        a = 10;
        System.out.println("a = " + a);
        //"APPLE" 문자열 저장하고 출력
        java.lang.String s;  // java.lang 패키지 구문은 생략 가능
        s = "APPLE";
        System.out.println("s = " + s);
        System.out.println("s의 단어길이 = " + s.length());
    }
}
 